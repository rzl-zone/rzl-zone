import { CheckSquare, Copy } from "@rzl-zone/docs-ui/components/icons/lucide";

import { useCopyButtonFD } from "@workspace/fd-core/hooks/use-copy-button-fd";
import {
  UnstyledTabs,
  UnstyledTabsContent,
  type UnstyledTabsContentProps,
  UnstyledTabsList,
  type UnstyledTabsListProps,
  type UnstyledTabsProps,
  UnstyledTabsTrigger,
  type UnstyledTabsTriggerProps
} from "./tabs.unstyled";

export interface CodeBlockProps extends ComponentProps<"figure"> {
  /**
   * Icon of code block
   *
   * When passed as a string, it assumes the value is the HTML of icon
   */
  icon?: ReactNode | "disable";
  /**
   * Disable showing Icon ?
   *
   * @default false
   */
  noIcon?: boolean;

  /**
   * Allow to copy code with copy button
   *
   * @default true
   */
  allowCopy?: boolean;

  /**
   * Keep original background color generated by Shiki or Rehype Code
   *
   * @default false
   */
  keepBackground?: boolean;

  viewportProps?: HTMLAttributes<HTMLElement>;

  /**
   * show line numbers
   */
  "data-line-numbers"?: boolean;

  /**
   * @default 1
   */
  "data-line-numbers-start"?: number;

  /**
   * Identifier for Sharing value of tabs
   */
  groupId?: string;

  /**
   * Enable persistent
   */
  persist?: boolean;

  /**
   * If true, updates the URL hash based on the tab's id
   */
  updateAnchor?: boolean;

  Actions?: (props: { className?: string; children?: ReactNode }) => ReactNode;

  disableForceUnTabIndex?: boolean;
}

const TabsContext = createRequiredContext<{
  containerRef: RefObject<HTMLDivElement | null>;
  nested: boolean;
} | null>("TabsContext", null);

export function Pre(props: ComponentProps<"pre">) {
  return (
    <pre
      {...props}
      className={cn("min-w-full w-max *:flex *:flex-col", props.className)}
    >
      {props.children}
    </pre>
  );
}

export function CodeBlock({
  ref,
  title,
  groupId: _groupId,
  persist: _persist,
  updateAnchor: _updateAnchor,
  allowCopy = true,
  keepBackground = false,
  icon,
  noIcon = false,
  viewportProps = {},
  children,
  disableForceUnTabIndex = false,
  Actions = (props) => (
    <div
      {...props}
      className={cn("empty:hidden", props.className)}
    />
  ),
  ...props
}: CodeBlockProps) {
  const inTab = TabsContext.use() !== null;
  const areaRef = useRef<HTMLDivElement>(null);

  return (
    <figure
      {...props}
      ref={ref}
      dir="ltr"
      tabIndex={disableForceUnTabIndex ? props.tabIndex || -1 : -1}
      className={cn(
        inTab
          ? "bg-fd-card -mx-px -mb-px rounded-b-xl"
          : "my-2 bg-fd-card rounded-xl",
        keepBackground && "bg-(--shiki-light-bg) dark:bg-(--shiki-dark-bg)",
        !inTab && "shadow-md",
        "shiki relative border outline-none not-prose text-sm",
        props.className
      )}
    >
      <>
        {title ? (
          <div className="bg-fd-secondary flex text-fd-foreground/60 items-center gap-2 h-9.5 border-b px-4 font-medium rounded-t-xl">
            {!noIcon &&
              (typeof icon === "string" ? (
                <div
                  className={cn(inTab ? "[&_svg]:size-4" : "[&_svg]:size-3.5")}
                  dangerouslySetInnerHTML={{
                    __html: icon
                  }}
                />
              ) : (
                icon
              ))}
            <figcaption className="flex-1 truncate">{title}</figcaption>
            {Actions({
              className:
                "top-11.5 absolute right-2 z-4 backdrop-blur-md rounded-md bg-fd-card p-px",
              children: allowCopy && <CopyButton containerRef={areaRef} />
            })}
          </div>
        ) : (
          Actions({
            className:
              "top-2 absolute right-2 z-4 backdrop-blur-md rounded-md p-0.5",
            children: allowCopy && <CopyButton containerRef={areaRef} />
          })
        )}

        <ScrollArea
          scrollHideDelay={10}
          className={inTab || title ? "rounded-b-xl" : "rounded-xl"}
          viewPortOptions={{
            ...viewportProps,
            ref: areaRef,
            className: cn("text-sm py-3 max-h-150", viewportProps.className),
            style: {
              // space for toolbar
              "--padding-right": !title
                ? "calc(var(--spacing) * 8)"
                : undefined,
              counterSet: props["data-line-numbers"]
                ? `line ${Number(props["data-line-numbers-start"] ?? 1) - 1}`
                : undefined,
              ...viewportProps.style
            } as object
          }}
          scrollBarOptions={{ orientation: "all" }}
        >
          {children}
        </ScrollArea>
      </>
    </figure>
  );
}

function CopyButton({
  className,
  containerRef,
  ...props
}: ComponentProps<"button"> & {
  containerRef: RefObject<HTMLElement | null>;
}) {
  const [checked, onClick] = useCopyButtonFD({
    onCopy: () => {
      const pre = containerRef.current?.getElementsByTagName("pre").item(0);
      if (!pre) return;

      const clone = pre.cloneNode(true) as HTMLElement;
      clone.querySelectorAll(".nd-copy-ignore").forEach((node) => {
        node.replaceWith("\n");
      });

      void navigator.clipboard.writeText(clone.textContent ?? "");
    }
  });

  return (
    <button
      type="button"
      data-checked={checked || undefined}
      className={cn(
        buttonVariants({
          variant: "ghost",
          className: cn(
            "flex flex-row flex-1 gap-1 hover:text-fd-accent-foreground data-checked:text-fd-accent-foreground",
            "transform transition-colors duration-300 p-0",
            ""
            // "focus:outline-none focus:ring-0 focus:ring-non focus:ring-offset-0"
          ),
          size: "icon-xs"
        }),
        className
      )}
      aria-label={checked ? "Copied to clipboard" : "Copy to clipboard"}
      onClick={onClick}
      {...props}
    >
      <div className="relative size-6 min-w-6 min-h-6">
        <Transition
          show={!checked}
          enter="transition-all duration-300 ease-out"
          enterFrom="opacity-0 scale-75"
          enterTo="opacity-100 scale-100"
          leave="transition-all duration-300 ease-in"
          leaveFrom="opacity-100 scale-100"
          leaveTo="opacity-0 scale-75"
          // unmount={false}
        >
          <div className="absolute inset-0 flex items-center justify-center">
            <Copy />
            <span className="sr-only">Copy</span>
          </div>
        </Transition>

        <Transition
          show={checked}
          enter="transition-all duration-300 ease-out"
          enterFrom="opacity-0 scale-75"
          enterTo="opacity-100 scale-100"
          leave="transition-all duration-300 ease-in"
          leaveFrom="opacity-100 scale-100"
          leaveTo="opacity-0 scale-75"
          // unmount={false}
        >
          <div className="absolute inset-0 flex items-center justify-center">
            <CheckSquare />
            <span className="sr-only">Copied!</span>
          </div>
        </Transition>
      </div>
    </button>
  );
}

export function CodeBlockTabs({ ref, ...props }: UnstyledTabsProps) {
  const containerRef = useRef<HTMLDivElement>(null);
  const nested = TabsContext.use() !== null;

  return (
    <UnstyledTabs
      ref={mergeRefs(containerRef, ref)}
      {...props}
      className={cn(
        "[&>div]:first:bg-fd-secondary [&>div]:first:rounded-t-xl [&>div]:first:shadow-md [&>div]:first:border [&>div>figure:only-child]:shadow-md",
        !nested &&
          "my-2 [&>div>figure:only-child]:mt-0 [&>div>figure:only-child]:bg-fd-card [&>div>figure:only-child]:mx-0 [&>div>figure:only-child]:border [&>div>figure:only-child]:border-t-0",
        props.className
      )}
    >
      <TabsContext.Provider
        value={useMemo(
          () => ({
            containerRef,
            nested
          }),
          [nested]
        )}
      >
        {props.children}
      </TabsContext.Provider>
    </UnstyledTabs>
  );
}

export function CodeBlockTabsList(props: UnstyledTabsListProps) {
  return (
    <UnstyledTabsList
      {...props}
      className={cn(
        "flex flex-row px-2 overflow-x-auto text-fd-muted-foreground",
        props.className
      )}
    >
      {props.children}
    </UnstyledTabsList>
  );
}

export function CodeBlockTabsTrigger({
  children,
  ...props
}: UnstyledTabsTriggerProps) {
  return (
    <UnstyledTabsTrigger
      tabIndex={-1}
      {...props}
      className={cn(
        "relative group inline-flex text-sm font-medium text-nowrap items-center transition-colors gap-2 px-2 py-1.5 hover:text-fd-accent-foreground data-[state=active]:text-fd-primary [&_svg]:size-4",
        props.className
      )}
    >
      <div className="absolute inset-x-2 bottom-0 h-px group-data-[state=active]:bg-fd-primary" />
      {children}
    </UnstyledTabsTrigger>
  );
}

export function CodeBlockTab(props: UnstyledTabsContentProps) {
  return (
    <UnstyledTabsContent
      {...props}
      className={cn(props.className)}
    />
  );
}
